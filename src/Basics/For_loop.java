package Basics;

public class For_loop {
    public static void main(String[] args) {
                // Loop from 0 to 4 (5 times)
                for (int i = 0; i < 5; i++) {
                    System.out.println("Iteration: " + i);
                }

                System.out.println("\n--- Counting Down ---");
                // Loop from 5 down to 1
                for (int j = 5; j > 0; j--) {
                    System.out.println("Count: " + j);
                }
            }
        }
