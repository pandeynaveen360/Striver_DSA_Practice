package Basics;

public class Data_Types {
    public static void main(String[] args) {
        // Integer types
        byte myByte = 100; // Small integer
        short myShort = 20000; // Medium integer
        int myInt = 1234567890; // Standard integer

        // Floating-point types
        float myFloat = 3.14f; // Single-precision floating point (note the 'f' suffix)
        double myDouble = 123.456789012345; // Double-precision floating point (default for decimals)

        // Character type
        char myChar = 'A'; // Single character (enclosed in single quotes)
        char unicodeChar = '\u0042'; // Unicode character for 'B'

        // Boolean type
        boolean isJavaFun = true; // True or false
        boolean isLearningEasy = false;


        // String type
        String greeting = "Hello, Java!"; // Sequence of characters (enclosed in double quotes)
        String name = new String("Alice"); // Another way to create a String object


        // Array type (an array of integers)
        int[] numbers = {10, 20, 30, 40, 50}; // An array of 5 integers
        String[] fruits = {"Apple", "Banana", "Cherry"}; // An array of Strings
        }
    }

